regionSelect.addEventListener('change', (e) => {
    const region = e.target.value;
    const comunas = comunasPorRegion[region] || [];

    // Limpiar el select de comunas
    comunaSelect.innerHTML = '<option disabled selected value="">Selecciona la comuna</option>';

    // Agregar las nuevas opciones
    comunas.forEach(comuna => {
        const option = document.createElement('option');
        option.value = comuna;
        option.textContent = comuna;
        comunaSelect.appendChild(option);
    });
});

form.addEventListener('submit', (e) => {
    e.preventDefault();

    const comuna = comunaSelect.value;

    if (comuna === '') {
        showError('Ambos campos son obligatorios...');
        return;
    }

    callAPI(comuna);
});

function callAPI(comuna) {
    const apiId = 'a19dbba28322672a1c3f7e6026f808f9';
    const url = `http://api.openweathermap.org/data/2.5/weather?q=${comuna},CL&appid=${apiId}&units=metric&lang=es`;

    fetch(url)
        .then(response => response.json())
        .then(data => {
            if (data.cod === '404') {
                showError('Comuna no encontrada...');
            } else {
                clearHTML();
                showWeather(data);
            }
        })
        .catch(error => {
            console.error('Error fetching weather data:', error);
            showError('Error al obtener los datos del clima.');
        });
}

function showWeather(data) {
    const { name, main: { temp, temp_min, temp_max }, weather: [arr] } = data;

    const content = document.createElement('div');
    content.innerHTML = `
        <h5>Clima en ${name}</h5>
        <img src="https://openweathermap.org/img/wn/${arr.icon}@2x.png" alt="icon">
        <h2>${temp}°C</h2>
        <p>Max: ${temp_max}°C</p>
        <p>Min: ${temp_min}°C</p>
    `;

    result.appendChild(content);
}

function showError(message) {
    const alert = document.createElement('p');
    alert.classList.add('alert-message');
    alert.innerHTML = message;

    form.appendChild(alert);
    setTimeout(() => {
        alert.remove();
    }, 3000);
}

function clearHTML() {
    result.innerHTML = '';
}
